        (function () {
            var scripts = {};
            var bootstrapPromise;
            var jqPromise;
            var boostrapLoading = false;

            function loadScript(src) {
                if (scripts[src]) return scripts[src];
                scripts[src] = new Promise(function (resolve, reject) {
                    var s = document.createElement("script");
                    s.src = src;
                    s.async = true;
                    s.onload = resolve;
                    s.onerror = reject;
                    document.head.appendChild(s);
                });
                return scripts[src];
            }

            function ensureBootstrap() {
                if (window.bootstrap) return Promise.resolve();
                if (bootstrapPromise) return bootstrapPromise;
                bootstrapPromise = loadScript("https://cdn.jsdelivr.net/npm/bootstrap@5.3.8/dist/js/bootstrap.bundle.min.js");
                return bootstrapPromise;
            }

            function ensureJqSlick() {
                if (jqPromise) return jqPromise;
                jqPromise = loadScript("https://code.jquery.com/jquery-3.7.1.min.js")
                    .then(function () { return loadScript("/assets/js/slick.js"); })
                    .then(function () { return loadScript("/assets/js/custom.new.js"); });
                return jqPromise;
            }

            function observeOnce(selectors, cb) {
                var els = [];
                selectors.forEach(function (sel) {
                    document.querySelectorAll(sel).forEach(function (el) { els.push(el); });
                });
                if (!els.length) return;
                if (!("IntersectionObserver" in window)) {
                    window.addEventListener("scroll", function () { cb(); }, { once: true, passive: true });
                    window.addEventListener("touchstart", function () { cb(); }, { once: true, passive: true });
                    return;
                }
                var fired = false;
                var io = new IntersectionObserver(function (entries) {
                    if (fired) return;
                    for (var i = 0; i < entries.length; i++) {
                        if (entries[i].isIntersecting) {
                            fired = true;
                            io.disconnect();
                            cb();
                            break;
                        }
                    }
                }, { rootMargin: "200px" });
                els.forEach(function (el) { io.observe(el); });
            }

            if (window.matchMedia && window.matchMedia("(min-width: 992px)").matches) {
                observeOnce([".logoSlider", ".portfolioSlider", ".testimonial_slider"], ensureJqSlick);
            } else {
                observeOnce([".portfolioSlider"], ensureJqSlick);
            }

            document.addEventListener("click", function (e) {
                var target = e.target.closest("[data-bs-toggle],[data-bs-target],[data-bs-dismiss]");
                if (!target) return;
                if (window.bootstrap || boostrapLoading) return;
                boostrapLoading = true;
                e.preventDefault();
                e.stopPropagation();
                ensureBootstrap().then(function () {
                    boostrapLoading = false;
                    target.click();
                });
            }, true);
        })();
